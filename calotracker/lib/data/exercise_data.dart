  /// Lọc bài tập theo tiêu chí
  static List<ExerciseItem> filterExercises({
    ExerciseGoal? goal,
    ExerciseDifficulty? difficulty,
    ExerciseLocation? location,
  }) {
    return allExercises.where((exercise) {
      if (goal != null && exercise.goal != goal) return false;
      if (difficulty != null && exercise.difficulty != difficulty) return false;
      if (location != null && exercise.location != location) return false;
      return true;
    }).toList();
  }

  /// Lấy bài tập theo mục tiêu
  static List<ExerciseItem> getByGoal(ExerciseGoal goal) {
    return allExercises.where((e) => e.goal == goal).toList();
  }

  /// Lấy bài tập theo độ khó
  static List<ExerciseItem> getByDifficulty(ExerciseDifficulty difficulty) {
    return allExercises.where((e) => e.difficulty == difficulty).toList();
  }

  /// Lấy bài tập theo địa điểm
  static List<ExerciseItem> getByLocation(ExerciseLocation location) {
    return allExercises.where((e) => e.location == location).toList();
  }
