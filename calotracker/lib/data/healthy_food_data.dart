  /// Lọc theo category
  static List<HealthyFood> getByCategory(FoodCategory category) {
    return allFoods.where((f) => f.category == category).toList();
  }

  /// Tìm kiếm theo tên
  static List<HealthyFood> search(String query) {
    final q = query.toLowerCase();
    return allFoods
        .where(
          (f) =>
              f.name.toLowerCase().contains(q) ||
              f.description.toLowerCase().contains(q),
        )
        .toList();
  }

  /// Lấy thực phẩm ít calo nhất
  static List<HealthyFood> getLowCalorie({int maxCalories = 100}) {
    return allFoods.where((f) => f.caloriesPer100g <= maxCalories).toList()
      ..sort((a, b) => a.caloriesPer100g.compareTo(b.caloriesPer100g));
  }

  /// Lấy thực phẩm giàu protein
  static List<HealthyFood> getHighProtein({double minProtein = 15}) {
    return allFoods.where((f) => f.proteinPer100g >= minProtein).toList()
      ..sort((a, b) => b.proteinPer100g.compareTo(a.proteinPer100g));
  }
